import pygame as pg
from pygame.locals import *

class Control():
    def __init__():
        self.state_dict = {}
        self.state_name = None
        self.state = None
    def setup_status(state_dict, state_name):
        self.state_dict = state_dict
        self.state_name = state_name
        self.state = self.state_dict[self.state_name]

class State():
    def __init__():
        pass

class Start(State):
    def __init__():
        State.__init__()

class Play(State):
    def __init__():
        State.__init__()

class Result(State):
    def __init__():
        State.__init__



